      class="add-update-preview"
      ref="ruleForm"
      :model="ruleForm"
      :rules="rules"
      label-width="150px"
    >
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="城市" prop="cityname">
            <el-input v-model="ruleForm.cityname" 
                placeholder="城市" clearable :disabled=" false  ||ro.cityname"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="站点" prop="positionname">
            <el-input v-model="ruleForm.positionname" 
                placeholder="站点" clearable :disabled=" false  ||ro.positionname"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="AQI" prop="aqi">
            <el-input v-model.number="ruleForm.aqi" 
                placeholder="AQI" clearable :disabled=" false  ||ro.aqi"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="PM2_5" prop="pm25">
            <el-input v-model.number="ruleForm.pm25" 
                placeholder="PM2_5" clearable :disabled=" false  ||ro.pm25"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="PM10" prop="pm10">
            <el-input v-model.number="ruleForm.pm10" 
                placeholder="PM10" clearable :disabled=" false  ||ro.pm10"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="SO2" prop="so2">
            <el-input v-model.number="ruleForm.so2" 
                placeholder="SO2" clearable :disabled=" false  ||ro.so2"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="NO2" prop="no2">
            <el-input v-model.number="ruleForm.no2" 
                placeholder="NO2" clearable :disabled=" false  ||ro.no2"></el-input>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="CO" prop="co">
			<el-input-number v-model="ruleForm.co" placeholder="CO" :disabled="ro.co"></el-input-number>
          </el-form-item>
          <el-form-item :style='{"width":"100%","padding":"0px","margin":"0 0 20px","background":"none","display":"inline-block"}' label="O3" prop="o3">
            <el-input v-model.number="ruleForm.o3" 
