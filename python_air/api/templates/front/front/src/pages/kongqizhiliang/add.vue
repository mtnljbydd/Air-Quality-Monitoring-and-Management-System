<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				cityname : false,
				positionname : false,
				aqi : false,
				pm25 : false,
				pm10 : false,
				so2 : false,
				no2 : false,
				co : false,
				o3 : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          cityname: '',
          positionname: '',
          aqi: '',
          pm25: '',
          pm10: '',
          so2: '',
          no2: '',
          co: '',
          o3: '',
        },


        rules: {
          cityname: [
          ],
          positionname: [
          ],
          aqi: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          pm25: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          pm10: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          so2: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          no2: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          co: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          o3: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='cityname'){
              this.ruleForm.cityname = obj[o];
              this.ro.cityname = true;
              continue;
            }
            if(o=='positionname'){
              this.ruleForm.positionname = obj[o];
              this.ro.positionname = true;
              continue;
            }
            if(o=='aqi'){
              this.ruleForm.aqi = obj[o];
              this.ro.aqi = true;
              continue;
            }
            if(o=='pm25'){
              this.ruleForm.pm25 = obj[o];
              this.ro.pm25 = true;
              continue;
            }
            if(o=='pm10'){
              this.ruleForm.pm10 = obj[o];
              this.ro.pm10 = true;
              continue;
            }
            if(o=='so2'){
              this.ruleForm.so2 = obj[o];
              this.ro.so2 = true;
              continue;
            }
            if(o=='no2'){
              this.ruleForm.no2 = obj[o];
              this.ro.no2 = true;
              continue;
            }
            if(o=='co'){
              this.ruleForm.co = obj[o];
              this.ro.co = true;
              continue;
            }
            if(o=='o3'){
              this.ruleForm.o3 = obj[o];
              this.ro.o3 = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`kongqizhiliang/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {
			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('kongqizhiliang/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`kongqizhiliang/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`kongqizhiliang/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
    }
  };
</script>

